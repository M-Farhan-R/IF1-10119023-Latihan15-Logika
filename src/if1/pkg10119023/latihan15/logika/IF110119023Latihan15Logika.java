public class IF110119023Latihan15Logika {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println("a && b = " + (a && b)); //a AND b
        System.out.println("a || b = " + (a || b)); //a OR b
        System.out.println("!a = " + !a); //NOT a
        System.out.println("!(a && b) = " + !(a && b)); //NOT(a AND b)
    }
    
}
